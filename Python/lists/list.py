# Creating an empty list
my_list = []

# Inserting elements into the list
my_list.insert(0, "apple")
my_list.insert(1, "banana")
my_list.insert(2, "cherry")

print("After inserting elements:", my_list)

# Splitting a string into a list
fruits = "apple,banana,cherry"
fruits_list = fruits.split(",")
print("Split string into a list:", fruits_list)

# Appending an element to the list
my_list.append("date")
print("After appending an element:", my_list)

# Using range() to create a list of numbers
number_list = list(range(1, 6))
print("Number list using range():", number_list)

