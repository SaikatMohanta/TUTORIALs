#   Use of Escape Sequence in the print statement

print("1.hello \' world")   #1.hello ' world

print("2.hello \" world")   #2.hello " world

print("3.hello \t world")   #3.hello 	 world

print("4.hello \n world")   #4.hello 
#                              world

#print("5.hello \r world")
   
print("6.hello \a world")   #6.hello  world

print("7.hello \\ world")   #7.hello \ world

print("7.hello \f world")   #7.hello 
#                                     world

print("8.hello\v world")    #8.hello
#                                     world




