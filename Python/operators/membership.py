# Membership operations
numbers = [1, 2, 3, 4, 5]

# Membership in
membership_in = 3 in numbers
print("Membership In:", membership_in)

# Membership not in
membership_not_in = 6 not in numbers
print("Membership Not In:", membership_not_in)

# Membership in - strings
string = "Hello, World!"
membership_in_string = "o" in string
print("Membership In (String):", membership_in_string)

# Membership not in - strings
membership_not_in_string = "z" not in string
print("Membership Not In (String):", membership_not_in_string)

