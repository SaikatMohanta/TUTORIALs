# Assignment operations
a = 10

# Simple assignment
b = a
print("Simple Assignment:", b)

# Addition assignment
b += 5
print("Addition Assignment:", b)

# Subtraction assignment
b -= 3
print("Subtraction Assignment:", b)

# Multiplication assignment
b *= 2
print("Multiplication Assignment:", b)

# Division assignment
b /= 4
print("Division Assignment:", b)

# Modulo assignment
b %= 3
print("Modulo Assignment:", b)

# Exponentiation assignment
b **= 2
print("Exponentiation Assignment:", b)

# Floor division assignment
b //= 2
print("Floor Division Assignment:", b)

