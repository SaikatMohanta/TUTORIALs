# Addition
a = 10
b = 5
addition = a + b
print("Addition:", addition)

# Subtraction
subtraction = a - b
print("Subtraction:", subtraction)

# Multiplication
multiplication = a * b
print("Multiplication:", multiplication)

# Division
division = a / b
print("Division:", division)

# Floor Division
floor_division = a // b
print("Floor Division:", floor_division)

# Modulo
modulo = a % b
print("Modulo:", modulo)

# Exponentiation
exponentiation = a ** b
print("Exponentiation:", exponentiation)

# Negation
negation = -a
print("Negation:", negation)

# Compound Operations
c = 2
c += 3  # Equivalent to c = c + 3
print("Compound Addition:", c)

c -= 1  # Equivalent to c = c - 1
print("Compound Subtraction:", c)

c *= 2  # Equivalent to c = c * 2
print("Compound Multiplication:", c)

c /= 4  # Equivalent to c = c / 4
print("Compound Division:", c)

c **= 2  # Equivalent to c = c ** 2
print("Compound Exponentiation:", c)

