# Unary operations
a = 10

# Unary positive
unary_positive = +a
print("Unary Positive:", unary_positive)

# Unary negative
unary_negative = -a
print("Unary Negative:", unary_negative)

# Unary bitwise NOT
unary_bitwise_not = ~a
print("Unary Bitwise NOT:", unary_bitwise_not)

# Unary logical NOT
unary_logical_not = not a
print("Unary Logical NOT:", unary_logical_not)

