# Logical operations
a = True
b = False

# Logical AND
logical_and = a and b
print("Logical AND:", logical_and)

# Logical OR
logical_or = a or b
print("Logical OR:", logical_or)

# Logical NOT
logical_not_a = not a
logical_not_b = not b
print("Logical NOT a:", logical_not_a)
print("Logical NOT b:", logical_not_b)

